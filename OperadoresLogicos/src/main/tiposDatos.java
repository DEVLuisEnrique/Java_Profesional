package main;

public class tiposDatos {
	
	public static void main(String arg[]) {
		
	
		int var1 = 300, var2 = 300;
		boolean resultado;
		
		resultado = var1 > var2;
		System.out.println(resultado);
		
		resultado = var1 < var2;
		System.out.println(resultado);
		
		resultado = var1 >= var2;
		System.out.println(resultado);
		
		
		resultado = var1 <= var2;
		System.out.println(resultado);
		
		resultado = var1 == var2;
		System.out.println(resultado);
		
		resultado = var1 != var2;
		System.out.println(resultado);
	}

}
